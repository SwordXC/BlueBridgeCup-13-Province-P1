void led_on(uint8_t num)
{
	gpio=gpio & (~(0x80<<num));
	GPIOC->ODR=gpio|((GPIOC->ODR)& 0x00ff);
	HAL_GPIO_WritePin(GPIOD,GPIO_PIN_2,GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOD,GPIO_PIN_2,GPIO_PIN_RESET);
}
void led_off(uint8_t num)
{
	gpio=gpio | (0x80<<num);
	GPIOC->ODR=gpio | ((GPIOC->ODR)&0x00ff);
	HAL_GPIO_WritePin(GPIOD,GPIO_PIN_2,GPIO_PIN_SET);
	HAL_GPIO_WritePin(GPIOD,GPIO_PIN_2,GPIO_PIN_RESET);
}
void led_close()
{
	    gpio = 0xff00;
    GPIOC->ODR = gpio | ((GPIOC->ODR) & 0x00ff); 

    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, GPIO_PIN_SET);

    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_2, GPIO_PIN_RESET);

}
